    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        int qty = Integer.parseInt(jTextField2.getText());
        double price = Double.parseDouble(jTextField3.getText());
        System.out.println(price);
        double rowtotal = price * qty;
        total += price * qty;

        medicines.put("medicime" + medicine, new Medicine(name, qty, price, rowtotal));
        loadTable();
        medicine++;


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String path = "src/reports/Blank_A4.jasper";

        JRTableModelDataSource dataSourse = new JRTableModelDataSource(jTable1.getModel());

        HashMap<String, Object> parameters = new HashMap<>();
        parameters.put("Parameter1", jTextField4.getText());

        JasperPrint jasperPrint;
        try {
            jasperPrint = JasperFillManager.fillReport(path, parameters, dataSourse);
            JasperViewer.viewReport(jasperPrint,false);
        } catch (JRException ex) {
            Logger.getLogger(MedicationBilling.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

