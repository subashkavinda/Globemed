    void loadTable() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (int i = 0; i <= medicine; i++) {

            Medicine m = (Medicine) medicines.get("medicime" + i);
            Vector v = new Vector();

            v.add(m.getName());
            v.add(String.valueOf(m.getQty()));
            v.add(String.valueOf(m.getUnitPrice()));
            v.add(String.valueOf(m.getTotalPrice()));

            model.addRow(v);
        }
        jTextField4.setText(String.valueOf(total));
    }


